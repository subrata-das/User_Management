        $userInfo = UserInfo::where('user_id',$authId)->get();

        if(!$userInfo->isEmpty()){
            $user = User::with('userInfo')->findOrFail($authId);
            return view('auth.user.userHome')->with('userInfo',$user);
        } else {
            return redirect('userInfoRegister');
